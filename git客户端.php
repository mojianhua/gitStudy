/*
1、查看git状态
git status
2、添加文件到版本控制
git add 文件名，如果需要添加所有文件，则：git add .(英文句话)
3、提交
git commit -m ’注释’
4、git 初始化
git init
5、关联远程(origin)仓库
git remote add origin [git路径]
6、推送到远程仓库
git push -u origin master
7、版本回退
git reset —hard [版本号]
9、查看分支
git branch
10、创建分支
git branch [分支名]
11、切换分支
git checkout [分支名]
12、新建并且创建分支
git checkout -b [分支名]
13、合并分支
git merge [分支名]
